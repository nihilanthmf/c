// this function magically makes it so the terminal can read a character without the need for a user to press the Enter key
char getch(void) {
    struct termios oldt, newt;
    char ch;

    tcgetattr(STDIN_FILENO, &oldt);     // save current terminal attributes
    newt = oldt;                        // copy
    newt.c_lflag &= ~(ICANON | ECHO);   // disable canonical mode & echo
    newt.c_cc[VMIN] = 1;                // read() will return when 1 byte is available
    newt.c_cc[VTIME] = 0;               // no timeout

    tcsetattr(STDIN_FILENO, TCSANOW, &newt);  // apply settings
    read(STDIN_FILENO, &ch, 1);               // read 1 byte (unbuffered)
    tcsetattr(STDIN_FILENO, TCSANOW, &oldt);  // restore saved settings

    return ch;
}

void render(struct Position player_pos) {
    system("clear");
    for (int y = 0; y < HEIGHT; ++y) {
        for (int x = 0; x < WIDTH; ++x) {
            if (player_pos.x == x && player_pos.y == y) {
                printf("o ");
            } else if (env[y][x] == 1) {
                printf("■ ");
            } else if (env[y][x] == 2) {
                printf("# ");
            } else {
                printf("  ");
            }
        }
        printf("\n");
    }
}

void render_ui(int health) {
    printf("\n");
    for (int i = 0; i < health; ++i) {
        printf("■");
    }
    printf("\n");
}

char collision_check(struct Position next_player_pos) {
    if (env[next_player_pos.y][next_player_pos.x] == 1) {
        return 1; // wall
    } else if (env[next_player_pos.y][next_player_pos.x] == 2) {
        return 2; // enemy
    }
    return 0;
}

int main() {
    struct Position player_pos = {3, 4};
    struct Position temporary_player_pos = {player_pos.x, player_pos.y};
    int health = 10;

    while (1) {
        render(player_pos);
        render_ui(health);
        char c = getch();
        if (c == 'd') {
            temporary_player_pos.x++;
        } else if (c == 'a') {
            temporary_player_pos.x--;
        } else if (c == 'w') {
            temporary_player_pos.y--;
        } else if (c == 's') {
            temporary_player_pos.y++;
        }

        char collision = collision_check(temporary_player_pos);
        
        if (collision == 1) {
            temporary_player_pos.x = player_pos.x;
            temporary_player_pos.y = player_pos.y;
        } else {
            player_pos.x = temporary_player_pos.x;
            player_pos.y = temporary_player_pos.y;

            if (collision == 2) {
                --health;
            }
        }
    }
}
